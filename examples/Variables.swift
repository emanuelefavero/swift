// Variables
var name = "John"
print("Hello \(name)")
name = "Jane"
print("Hello \(name)")

// Constants
let pi = 3.14159
print("The value of pi is \(pi)")

// Type annotations
// Swift can infer the type of a variable, but you can also specify it explicitly:
var age: Int = 30
print("I am \(age) years old")
