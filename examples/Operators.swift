// * Operators

// Arithmetic operators
print(1 + 2) // 3
print(2 - 1) // 1
print(2 * 3) // 6
print(10.0 / 2.5) // 4.0

// Remainder operator
print(10 % 3) // 1, the remainder of 10 divided by 3

// Unary operators
print(2 << 1) // 4, shift left
print(2 >> 1) // 1, shift right

// Assignment operators
print(1 == 1) // true
print(1 != 1) // false

// Comparison operators
print(1 > 2) // false
print(1 < 2) // true
print(1 >= 1) // true
print(1 <= 1) // true

// Logical operators
print(true && false) // false
print(true || false) // true

// Negation
print(!true) // false

// Ternary operator
print(2 > 0 ? "Yes" : "No") // Yes

// Nil-coalescing operator
var m = 2
print(m ?? 0) // 2
var n: Int? = nil
print(n ?? 0) // 0
